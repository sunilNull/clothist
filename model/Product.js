class Product {
    constructor(id, name, brand, price, image, totalReviews, stars, isNew, isOnSale){
        this.id = id
        this.name = name
        this.brand = brand
        this.price = price
        this.image = image
        this.totalReviews = totalReviews
        this.stars = stars
        this.isNew = isNew
        this.isOnSale = isOnSale
    }
}

export default Product;